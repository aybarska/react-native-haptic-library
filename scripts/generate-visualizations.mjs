#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const rootDir = resolve(new URL('..', import.meta.url).pathname);
const patternsPath = resolve(rootDir, 'src/patterns.ts');
const generatedPatternsPath = resolve(rootDir, 'generated/core-haptics.patterns.json');
const outputPath = resolve(rootDir, 'src/pattern-visualizations.ts');

const basicVisualizations = {
  selection: impulse(80, [{ time: 0, amplitude: 0.45, frequency: 0.55 }]),
  soft: impulse(90, [{ time: 0, amplitude: 0.35, frequency: 0.2 }]),
  rigid: impulse(90, [{ time: 0, amplitude: 0.65, frequency: 0.95 }]),
  light: impulse(90, [{ time: 0, amplitude: 0.38, frequency: 0.55 }]),
  medium: impulse(110, [{ time: 0, amplitude: 0.58, frequency: 0.55 }]),
  heavy: impulse(130, [{ time: 0, amplitude: 0.86, frequency: 0.45 }]),
  success: impulse(260, [
    { time: 0, amplitude: 0.48, frequency: 0.55 },
    { time: 90, amplitude: 0.56, frequency: 0.65 },
    { time: 180, amplitude: 0.74, frequency: 0.8 },
  ]),
  error: impulse(300, [
    { time: 0, amplitude: 0.85, frequency: 0.2 },
    { time: 120, amplitude: 0.9, frequency: 0.18 },
    { time: 240, amplitude: 0.72, frequency: 0.22 },
  ]),
  warning: impulse(220, [
    { time: 0, amplitude: 0.72, frequency: 0.45 },
    { time: 140, amplitude: 0.55, frequency: 0.5 },
  ]),
};

function impulse(durationMillis, impacts) {
  return {
    durationMillis,
    envelope: { amplitude: [], frequency: [] },
    impacts,
  };
}

function parsePatternNames(source) {
  const match = source.match(/export const patternNames = (\[[\s\S]*?\]) as const;/);
  if (!match) {
    throw new Error('Could not find patternNames in src/patterns.ts');
  }
  return JSON.parse(match[1]);
}

function rounded(value) {
  return Math.round(Number(value) * 10000) / 10000;
}

function normalizeVisualization(pattern) {
  return {
    durationMillis: Math.max(30, Math.round(pattern.durationMillis)),
    envelope: {
      amplitude: pattern.envelope.amplitude.map((point) => ({
        time: Math.round(point.time),
        value: rounded(point.value),
      })),
      frequency: pattern.envelope.frequency.map((point) => ({
        time: Math.round(point.time),
        value: rounded(point.value),
      })),
    },
    impacts: pattern.impacts.map((point) => ({
      time: Math.round(point.time),
      amplitude: rounded(point.amplitude),
      frequency: rounded(point.frequency),
    })),
  };
}

function generateSource(visualizations) {
  return `// Generated by scripts/generate-visualizations.mjs from generated/core-haptics.patterns.json.
// Do not edit by hand.
import type { HapticPatternName } from './patterns';
import type { HapticVisualization } from './types';

export const patternVisualizations = ${JSON.stringify(visualizations, null, 2)} satisfies Record<HapticPatternName, HapticVisualization>;
`;
}

const patternNames = parsePatternNames(readFileSync(patternsPath, 'utf8'));
const generatedPatterns = JSON.parse(readFileSync(generatedPatternsPath, 'utf8'));
const generatedByName = new Map(
  generatedPatterns.patterns.map((pattern) => [pattern.name, normalizeVisualization(pattern)])
);

const visualizations = {};
const missing = [];

for (const name of patternNames) {
  const visualization = generatedByName.get(name) ?? basicVisualizations[name];
  if (!visualization) {
    missing.push(name);
    continue;
  }
  visualizations[name] = visualization;
}

if (missing.length > 0) {
  throw new Error(`Missing visualizations for: ${missing.join(', ')}`);
}

writeFileSync(outputPath, generateSource(visualizations));
console.log(`Generated ${Object.keys(visualizations).length} haptic visualizations at ${outputPath}`);
